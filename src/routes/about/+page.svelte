<h1 class="text-4xl text-center font-header my-8 uppercase text-transparent bg-clip-text bg-gradient-to-br from-blue-400 to-red-400">The Current Club System Needs Work.</h1>
<div class="my-1 flex flex-col">
    <h2 class="text-2xl font-header font-bold text-center uppercase">The Problem</h2>
    <p class="text-center place-self-center leading-relaxed my-2 w-[60rem]">
        The barrier of entry to starting a club is very high.
        <br>
        There are good things (Quality over Quantity, GCU gets more control, etc). <br>And bad things (Niche clubs don't take off, GCU gets more control).
    </p>
    <hr class="border-black border-[1.5px] my-2 w-52 place-self-center" />
    <div class="flex flex-row leading-relaxed py-3">
        <div class="w-[60rem]">
            <h3 class="font-header font-bold text-xl">GCU Process</h3>
            <ol class="px-5 list-disc">
                <li>Fill out full application (it's 10 pages) including club officers, advisor information, mission and vision, and 50 GCU student signatures (and their student ids)
                    of support for the club. (These signatures do not have to be future members of the club, but rather,
                    students who want to see the club succeed).
                </li>
                <li>
                    Turn in completed application to clubs@gcu.edu or bring it in person to Building 26, Student
Engagement Office.
                </li>
                <li>
                    The Clubs Committee will go through the approval process and reach out to you with any questions or
concerns.
                </li>
                <li>
                    Once the Clubs Committee has passed it, the application will then go to our student senate
for them to write a bill and give final approval at a Senate meeting.
                </li>
                <li>
                    Once approved at the Senate
meeting, the club officers will be assigned a club advocate who will meet with the club to train them and
get them acquainted with club policies and procedures.
                </li>
            </ol>
        </div>
        <div class="grow">
            <h3 class="font-header font-bold text-xl">Clubspace Process</h3>
            <ol class="px-5 list-disc">
                <li>Fill out the Easy Club Charter Form</li>
                <li>Wait for it to Get Approved (Takes Between 24-48 Hours).</li>
                <li>Done! Grow your club and make it an official club one day.</li>
            </ol>
        </div>
    </div>
    <p class="text-justify m-2">The current process is buried in bureaucracy and makes it hard to start a club.</p>
    
    <p class="text-justify m-2">
        This also affects the student experience by making it difficult for students to find people that share their interests through clubs since those clubs simply don't exist.
    </p>
    
    <p class="text-justify m-2">We give a platform where you can share your club and others can find it.</p>

    <h2 class="text-2xl font-header mt-4 font-bold text-center uppercase">Rules</h2>
    <hr class="border-black border-[1.5px] my-2 w-52 place-self-center" />
    <ol class="list-decimal m-2 place-self-center">
        <li>All clubs must adhere to the GCU policies outlined in the student handbook.</li>
        <li>Profanity is not acceptable in club titles or descriptions.</li>
        <li>Clubspace reserves the right to take down any club if deemed neccesary.</li>
    </ol>

</div>